        <h2 style="margin-top: 10%"><u>Management of fruit tree after planting</u></h2><br>
        <h3><u>Irrigation</u></h3>
        <p>Watering should be done immediately after planting in the field to foster
            proper establishment.</p>
        <p>If possible; you can water your mango tree at interval of 8 to 12 days 
            from when the mango tree start to produce flowers to maturity so as to 
            have higher yields.</p>
        <p>Note; watering is not recommended 2 to 3 months before flowering as it 
            will encourage increased branching and leaves development.
            </p>
        <br>
        <h3><u>Protection</u></h3>
        <p>Where the fruit tree is planted on an open field particularly where 
            animals are also grazed, it is advisable to protect the fruit tree against 
            destruction by livestock, especially goats, by building a fence of sticks/ 
            net around the planted fruit tree.</p>
        <br>
        <h3><u>Manure and Fertilizer application</u></h3>
        <p>After about 3-6 months, start application of nitrogenous fertilizers (in 
            case of fruit trees e.g. mango, pawpaw, oranges). Apply nitrogenous 
            fertilizers in varied intervals to avoid leaching out (downward movement 
            of useful mineral or nutrients in water). Alternatively, properly prepared 
            farm yard manure or compost, kitchen ash (left from cooking) and 
            mulching can be used in place of fertilizers.
            </p>
