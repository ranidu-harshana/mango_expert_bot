            <div class="tab-pane fade" id="nav-hwt" role="tabpanel" aria-labelledby="nav-hwt-tab">
              <h4>Hot water treatment (HWT)</h4>
              <p>HWT is the most effective post-harvest treatment against the main post harvest diseases of mango. During HWT, mature fruits are dipped for 5 to10 minutes in water heated to 52 co-55co. At this temperature range, the disease- causing organisms are killed without injuring the fruit.</p>
            </div>
            <div class="tab-pane fade" id="nav-ripening" role="tabpanel" aria-labelledby="nav-ripening-tab">
              <p>Fruits can be ripened by exposing them to ethral, which initiate the early and uniform ripening process.</p>
              <p>Now a day mango traders use calcium carbide for ripening. Calcium carbide contains impurities of arsenic and phosphorus hydrides which are toxic to human health.</p>
              <p>But ethylene is a natural hormone does not pose any health hazard for consumers of the fruits.</p>
            </div>
            <div class="tab-pane fade" id="nav-ethrel" role="tabpanel" aria-labelledby="nav-ethrel-tab">
              <h4>Using Ethrel for fruit ripening method</h4>
              <p>Sealed container.</p>
              <p>A solution which dissolved 1 ml ethrel in 1l of water.</p>
              <p>A solution much dissolved in 10g of  NaOH in 1 l of water.</p>
              <p>Place the fruits inside the ripening chamber with facilitate air circulation inside the chamber and then room should be sealed. Leave 1/5 of the chamber free from mangoes.</p>
              <p>Use 110-150 ml of ethrel for 1m3 of volume. Keep the ethrel container inside the chamber and pour NaOH solution into it. The volume of NaOH  is 185 ml of NaOH for 1m3 of volume of the chamber. close the container tightly just after putting NaOH.</p>
              <p>After 24 hours open the chamber and facilitate aeration.</p>
              <p>Ethrel can be stored in a amber color bottle for few months.</p>
