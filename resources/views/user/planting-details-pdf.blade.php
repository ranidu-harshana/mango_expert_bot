        <h3 style="margin-top: 10%"><u>Pruning and training</u></h3>
        <p>The height and form of the fruit tree needs to be controlled. This is meant 
            to guide the tree and facilitate its harvesting at its later stages. It is
            advisable that you carry out pruning in the first year to guide tree into 
            desired shape, and when trees are about 1 m from the ground, cap seedling 
            to encourage side branches. Carry out pruning later for proper tree
            maintenance and should be carried out after fruit harvest. Control height 
            to about 3.5 m and all branches at knee level (about 0.5m) be pruned. 
            Remove all dead branches to allow sunlight through canopy to the ground 
            under the tree.
            </p>
            <br>
        <h3><u>Flowering and fruit formation</u></h3>
        <p>Grafted fruit trees usually start to flower within two years from planting. 
            However, fruit formation should be discouraged at this stage as it can 
            affect growth of the fruit tree. For grafted mangoes, it is advisable that 
            fruit formation is allowed from the fourth year onwards.</p>
            <br>
        <h3><u>Pests and diseases</u></h3>
        <p>Fruit fly is the most common pests for mangoes and other fruits usually 
            causing a lot of damage to and high losses of these fruits. The use of fruit 
            fly traps is popular method of controlling fruit fly among other ways. 
            Other pests include gall midges and scales insects. Powdery mildew and 
            anthracnose are the most common mango diseases mainly affecting the 
            flowers, tender leaves and leading to development of black lesions on 
            mango fruits. Powdery mildew can be controlled by spraying using 
            Sulphur or Bayleton, while anthracnose can be controlled using copper
            fungicides.</p>
