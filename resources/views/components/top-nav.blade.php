            <div class="tab-pane fade show active" id="nav-irrigation" role="tabpanel" aria-labelledby="nav-irrigation-tab">
              <p>Water the trees right after planting and during drought periods. If dry season comes just after planting, water the trees regularly at least of 3 days interval.</p>
              <p>The watering of trees after 3 years of age should be managed in order to get higher yield. The watering should be stopped before 3-4 months of flowing initiation. If prolong adverse drought period exists watering should be done only for surviving the plants.</p>
              <p>With the initiation of flowering watering should be practiced regularly up to fruit maturing stage.</p>
            </div>
            <div class="tab-pane fade" id="nav-pruning" role="tabpanel" aria-labelledby="nav-pruning-tab">
              <p>Better canopy management is essential to have good productivity of any mango variety. In mango, increase in production with enhanced fruit quality is achieved by managing canopies.</p>
              <p>
                The pruning should aim for,
                <ol>
                  <li>Maximum utilization of light.</li>
                  <li>Avoidance of built- up microclimate spots for diseases & post late infestation.</li>
                  <li>Convenience in carrying out the cultural practices.</li>
                  <li>Maximizing the productivity with quality fruit production.</li>
                </ol>
              </p>

            </div>
            <div class="tab-pane fade" id="nav-training-tree" role="tabpanel" aria-labelledby="nav-training-tree-tab">
              <ul>
                <li>Allow the trees to grow upright for some time.</li>
                <li>When about 50 cm high, pinch the terminal bud to force lateral shoots to sprout.</li>
                <li>Allow the shoots to mature and again pinch the terminal bud on each shoot.</li>
                <li>Repeat this process until the end of the 3rd</li>
                <li>Remove low hanging branches, twigs and disorderly branches inside the tree to open up the tree canopy and any diseased and dead branches.</li>
              </ul>
            </div>
