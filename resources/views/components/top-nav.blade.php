              <h4>Determination of harvesting period</h4>
              <p>Changes of external appearances of matured mango fruits vary from variety to variety.</p>
              <p>Color change from dark green to light green, appearance of yellow color on either side of the green shoulder, conspicuous shoulders and oozing thick wax from petiole are the common indexes for mature mangoes. </p>
