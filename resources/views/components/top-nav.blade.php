              <ul><li>Paclobutrazol is applied to induce flowing.</li></ul>
              <p>This should be applied at correct stage with correct concentration of chemical to get better results.</p>
              <p><b>Harvesting and post-harvest indices of mango.</b></p>
              <p>The quality and the postharvest standard of the mango fruit depends on the time and stage of maturity at harvest.</p>
              <p>The number of days from flowering and fruit set to harvest is a good nondestructive maturity index for mangoes.  </p>
